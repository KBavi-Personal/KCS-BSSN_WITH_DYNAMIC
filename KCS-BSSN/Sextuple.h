#pragma once
template <class T1, class T2, class T3, class T4, class T5, class T6, class T7>
class Sextuple {
public:
	T1 keyword;
	T2 pi;
	T3 omega;
	T4 social;
	T5 structrual;
	T6 spacial;
	T7 influnce;
	Sextuple(T1 f, T2 s, T3 t, T4 fo, T5 fi, T6 si, T7 se) {
		keyword = f;
		pi = s;
		omega = t;
		social = fo;
		structrual = fi;
		spacial = si;
		influnce = se;
		
	}
};